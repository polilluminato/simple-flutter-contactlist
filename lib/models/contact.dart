class Contact {
  final String name;
  final String surname;
  final String phone;
  final String email;
  final String address;
  final String city;
  final String username;
  final String prefix;

  Contact({
    required this.name,
    required this.surname,
    required this.phone,
    required this.email,
    required this.address,
    required this.city,
    required this.username,
    required this.prefix,
  });
}